// Variáveis

/* Variável cria um espaço na memória
   - Representado por:
   var // É variável, pode mudar na aplicação
   let // Assim como var, pode mudar na aplicação
      var clima = "Verão"
      clima = "Inverno"
      console.log(clima);

   const // É uma constante, ou seja, não pode mudar seu valor ou seu tipo na aplicação
      const clima = "Verão"
      clima = "Inverno"
      console.log(clima);

   Typeof você pode alterar o tipo de uma variável (var/let) na aplicação.
      const clima = true // boolean
      clima = "" // string
      console.log(clima);

   Obs: variáveis são por var padrão, caso não especificar, será atribuida como var
*/
